public class KelompokModel {
    
    private String id;
    private String kelompokNama;
    private String tglDibuat;
    private String jmlMember;
    private String namaKetua;
    private int status;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getKelompokNama() {
        return kelompokNama;
    }

    public void setKelompokNama(String kelompokNama) {
        this.kelompokNama = kelompokNama;
    }

    public String getTglDibuat() {
        return tglDibuat;
    }

    public void setTglDibuat(String tglDibuat) {
        this.tglDibuat = tglDibuat;
    }

    public String getJmlMember() {
        return jmlMember;
    }

    public void setJmlMember(String jmlMember) {
        this.jmlMember = jmlMember;
    }

    public String getNamaKetua() {
        return namaKetua;
    }

    public void setNamaKetua(String namaKetua) {
        this.namaKetua = namaKetua;
    }

    public int getStatus() {
        return status;
    }

    public void setStatus(int status) {
        this.status = status;
    }
    
    
    
}
